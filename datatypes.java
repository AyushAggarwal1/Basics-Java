public class datatypes
{
    public static void main(final String[] args) {
        System.out.println("the data types with ranges and size are  ; ");
        System.out.print("s no. 1 ; ");
        System.out.print(" data type : int ; ");
        System.out.print("range : 2147483647");
        System.out.print(" to -2147483648");
        System.out.println(" ; size : 4");
        System.out.print("s no. 2 ; ");
        System.out.print(" data type : float ; ");
        System.out.print("range 3.4028235E38");
        System.out.print(" to 1.4E-45");
        System.out.println(" ; size : 4");
        System.out.print("s no. 3 ; ");
        System.out.print(" data type : char ; ");
        System.out.print("range \uffff");
        System.out.print(" to \u0000");
        System.out.println(" ; size : 2");
    }
}